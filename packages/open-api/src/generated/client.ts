import createClient from "openapi-fetch";
import type { paths } from "./schema"; // generated by openapi-typescript

const client = createClient<paths>({ baseUrl: "http://localhost:3000/" });



export async function getComplects() {
    const { data, error } = await client.GET("/api/offer-templates", {
        params: {
            query: {
                visibility: "public",
                portal_id: "1",
                is_active: true,
                search: "",
            },
        },

    });
    console.log(data);
    console.log(error);
    return data;
}

